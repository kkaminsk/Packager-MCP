function Uninstall-ADTDeployment {
    [CmdletBinding()]
    param()

    # Pre-Uninstall
    $adtSession.InstallPhase = "Pre-$($adtSession.DeploymentType)"
    Write-ADTLogEntry -Message "=== Pre-Uninstallation Phase ==="

    if ($adtSession.AppProcessesToClose.Count -gt 0) {
        Show-ADTInstallationWelcome -CloseProcesses $adtSession.AppProcessesToClose -CloseProcessesCountdown 60
    }
    Show-ADTInstallationProgress -StatusMessage 'Uninstalling WinRAR...'

    # Uninstall
    $adtSession.InstallPhase = $adtSession.DeploymentType
    Write-ADTLogEntry -Message "=== Uninstallation Phase ==="

    $app = Get-ADTApplication -Name 'WinRAR'

    if ($app -and $app.UninstallString) {
        # Parse uninstall string
        $uninstallCmd = $app.UninstallString
        Write-ADTLogEntry -Message "Uninstall command: $uninstallCmd"

        # Extract path (handle quoted paths)
        if ($uninstallCmd -match '^"([^"]+)"(.*)$') {
            $uninstallPath = $Matches[1]
        } elseif ($uninstallCmd -match '^(\S+)(.*)$') {
            $uninstallPath = $Matches[1]
        } else {
            $uninstallPath = $uninstallCmd
        }

        if (Test-Path $uninstallPath) {
            Start-ADTProcess -FilePath $uninstallPath -ArgumentList $UninstallArgs
        } else {
            Write-ADTLogEntry -Message "Uninstaller not found at: $uninstallPath" -Severity 2
        }
    } else {
        Write-ADTLogEntry -Message "Application not found or no uninstall string available" -Severity 2
    }

    # Post-Uninstall
    $adtSession.InstallPhase = "Post-$($adtSession.DeploymentType)"
    Write-ADTLogEntry -Message "=== Post-Uninstallation Phase ==="

    # CUSTOMIZE: Clean up remaining files and registry
