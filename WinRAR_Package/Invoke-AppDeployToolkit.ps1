}

function Repair-ADTDeployment {
    [CmdletBinding()]
    param()

    # Pre-Repair
    $adtSession.InstallPhase = "Pre-$($adtSession.DeploymentType)"
    Write-ADTLogEntry -Message "=== Repair Phase ==="

    if ($adtSession.AppProcessesToClose.Count -gt 0) {
        Show-ADTInstallationWelcome -CloseProcesses $adtSession.AppProcessesToClose -CloseProcessesCountdown 60
    }
    Show-ADTInstallationProgress -StatusMessage 'Repairing WinRAR...'

    # Repair
    $adtSession.InstallPhase = $adtSession.DeploymentType

    # For most EXE installers, repair means reinstall
    Write-ADTLogEntry -Message "Performing repair by reinstalling"

    # Uninstall first
    $app = Get-ADTApplication -Name 'WinRAR'
    if ($app.UninstallString) {
        $uninstallPath = ($app.UninstallString -split ' ')[0] -replace '"', ''
        if (Test-Path $uninstallPath) {
            Start-ADTProcess -FilePath $uninstallPath -ArgumentList $UninstallArgs
            Start-Sleep -Seconds 5
        }
    }

    # Reinstall
    $installerPath = Join-Path -Path $adtSession.DirFiles -ChildPath $InstallerFile
    Start-ADTProcess -FilePath $installerPath -ArgumentList $SilentArgs

    # Post-Repair
    $adtSession.InstallPhase = "Post-$($adtSession.DeploymentType)"
    Write-ADTLogEntry -Message "Repair completed"
}
